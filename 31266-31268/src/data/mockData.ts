const DANMAKU_TYPES: DanmakuType[] = [
  DanmakuType.SCROLL,
  DanmakuType.SCROLL,
  DanmakuType.SCROLL,
  DanmakuType.SCROLL,
  DanmakuType.TOP,
  DanmakuType.BOTTOM,
  DanmakuType.COLOR,
  DanmakuType.SPECIAL,
];

const EFFECT_TYPES: DanmakuEffectType[] = [
  DanmakuEffectType.GLOW,
  DanmakuEffectType.SHAKE,
  DanmakuEffectType.RAINBOW,
  DanmakuEffectType.BOUNCE,
];

export const generateRandomDanmaku = (): Danmaku => {
  const type = randomItem(DANMAKU_TYPES);
  const hasEffect = type === DanmakuType.SPECIAL || Math.random() < 0.1;

  return {
    id: generateId(),
    type,
    content: randomItem(DANMAKU_CONTENTS),
    color: randomItem(PRESET_COLORS),
    fontSize: 20 + randomInt(0, 8),
    speed: randomRange(80, 180),
    opacity: randomRange(0.7, 1),
    userId: `user_${randomInt(1000, 9999)}`,
    userName: randomItem(USER_NAMES),
    timestamp: Date.now(),
    playTime: 0,
    likes: randomInt(0, 100),
    isBlocked: false,
    effects: hasEffect
      ? {
          type: randomItem(EFFECT_TYPES),
          intensity: randomRange(0.3, 0.8),
        }
      : undefined,
  };
};

export const generateBatchDanmaku = (count: number): Danmaku[] => {
  const danmakus: Danmaku[] = [];
  for (let i = 0; i < count; i++) {
    const danmaku = generateRandomDanmaku();
    danmaku.timestamp = Date.now() + i * 50;
    danmakus.push(danmaku);
  }
  return danmakus;
};

